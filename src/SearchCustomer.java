    private void findButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findButtonActionPerformed
        String id = txtCustomerID.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            pst = con.prepareStatement("select * from customer where id = ?");
            pst.setString(1, id);
            ResultSet rs = pst.executeQuery();
            
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
            else{
                String firstName = rs.getString("first_name");
                String lastName = rs.getString("last_name");
                String passportID = rs.getString("passport_id");
                String address = rs.getString("address");
                
                String dob = rs.getString("dob");
                Date date1 = new SimpleDateFormat("yyyy-MM-dd").parse(dob);
                
                Blob blob = rs.getBlob("photo");
                byte[ ] imageBytes = blob .getBytes(1, (int) blob.length());
                ImageIcon image = new ImageIcon(imageBytes);
                Image im = image.getImage();
                Image myImg = im.getScaledInstance(labelPhoto.getWidth(), labelPhoto.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon newImage = new ImageIcon(myImg);
                
                
                String gender = rs.getString("gender");

                if(gender.equals("Female")){
                    radioBtn1.setSelected(false);
                    radioBtn2.setSelected(true);
                }else{
                    radioBtn1.setSelected(true);
                    radioBtn2.setSelected(false);
                }
                
                String contact = rs.getString("contact");
                
                txtFirstName.setText(firstName.trim());
                txtLastName.setText(lastName.trim());
                txtPassportID.setText(passportID.trim());
                txtAddress.setText(address.trim());
                txtContact.setText(contact.trim());
                
                txtDob.setDate(date1);
                labelPhoto.setIcon(newImage);
                
                
                
                
                
            }
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_findButtonActionPerformed

