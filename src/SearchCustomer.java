    private void browseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseButtonActionPerformed
        
        try{
            JFileChooser imgChooser = new JFileChooser();
            imgChooser.showOpenDialog(null);
            File img = imgChooser.getSelectedFile();
        
            FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images","png","jpg");
            imgChooser.addChoosableFileFilter(filter);
        
            path = img.getAbsolutePath();
            BufferedImage bimg;
            bimg = ImageIO.read(imgChooser.getSelectedFile());
            ImageIcon imageIcon = new 
            ImageIcon(new ImageIcon(bimg).getImage().getScaledInstance(250, 250, Image.SCALE_DEFAULT));
            labelPhoto.setIcon(imageIcon);
            
            File image = new File(path);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream baos = new ByteArrayOutputStream();
            byte[] buff = new byte[1024];
            for(int readNum; (readNum = fis.read(buff)) != -1;){
                baos.write(buff, 0, readNum);
            }
            userImage = baos.toByteArray();
            
        } catch(IOException ex){
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_browseButtonActionPerformed

    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
        String id = txtCustomerID.getText();
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String passport = txtPassportID.getText();
        String address = txtAddress.getText();
        
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String date = df.format(txtDob.getDate());
        
        String gender;
        
        if(radioBtn1.isSelected()){
            gender = "Male";
        }
        else{
            gender = "Female";
        }
        
        String contact = txtContact.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            pst = con.prepareStatement("update customer set first_name = ?, last_name = ?, passport_id = ?, address = ?, dob = ?, gender = ?, contact = ?, photo = ? where id = ?");
            
            pst.setString(1, firstName);
            pst.setString(2, lastName);
            pst.setString(3, passport);
            pst.setString(4, address);
            pst.setString(5, date);
            pst.setString(6, gender);
            pst.setString(7, contact);
            pst.setBytes(8, userImage);
            pst.setString(9, id);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Registration Updated");
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_buttonUpdateActionPerformed

