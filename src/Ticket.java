    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String source = txtSource.getSelectedItem().toString().trim();
        String departure = txtDeparture.getSelectedItem().toString().trim();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            pst = con.prepareStatement("select * from flight where source = ? and departure = ?");
            pst.setString(1, source);
            pst.setString(2, departure);
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsmd = rs.getMetaData();
            int c;
            c = rsmd.getColumnCount();
            
            DefaultTableModel dtm = (DefaultTableModel)tableFlightDetails.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                
                for(int i = 1; i<= c; i++){
                    v2.add(rs.getString("flight_id"));
                    v2.add(rs.getString("flight_name"));
                    v2.add(rs.getString("source"));
                    v2.add(rs.getString("departure"));
                    v2.add(rs.getString("date"));
                    v2.add(rs.getString("departure_time"));
                    v2.add(rs.getString("arrival_time"));
                    v2.add(rs.getString("flight_charge"));
                }
                
                dtm.addRow(v2);
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Ticket.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Ticket.class.getName()).log(Level.SEVERE, null, ex);
        }
       
            
       
    }//GEN-LAST:event_btnSearchActionPerformed

