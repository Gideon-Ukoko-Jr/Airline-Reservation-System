    private void bookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookButtonActionPerformed
        // TODO add your handling code here:
        String ticketID = labelTicketNo.getText();
        String flightID = labelFlightNoRep.getText();
        String customerID = txtCustomerID.getText();
        String flightClass = chooseClass.getSelectedItem().toString().trim();
        String price = txtPrice.getText();
        String seats = spinnerSeats.getValue().toString();
        
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String date = df.format(txtDateChooser.getDate());
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            pst = con.prepareStatement("insert into ticket(id, flight_id, customer_id, class, price, seats, date)values(?,?,?,?,?,?,?)");
            
            pst.setString(1, ticketID);
            pst.setString(2, flightID);
            pst.setString(3, customerID);
            pst.setString(4, flightClass);
            pst.setString(5, price);
            pst.setString(6, seats);
            pst.setString(7, date);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Ticket Booked");
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Ticket.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Ticket.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bookButtonActionPerformed

